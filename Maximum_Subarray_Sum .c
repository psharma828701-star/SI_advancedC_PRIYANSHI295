#include<stdio.h>

int main() 
{
    int arr[] = {-2, -3, 4, -1, -2, 1, 5, -3};
    int n = 8;
    int max = arr[0];
    int sum = 0;

    for (int i = 0; i < n; i++) 
{
        sum = sum + arr[i];

        if (sum > max) {
            max = sum;
        }

        if (sum < 0) 
{
            sum = 0;
        }
    }

    printf("Largest sum = %d", max);

    return 0;
}
